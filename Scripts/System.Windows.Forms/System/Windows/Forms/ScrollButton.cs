	Up = 0,
	/// <summary>A down-scroll arrow.</summary>
	Down = 1,
	/// <summary>A left-scroll arrow.</summary>
	Left = 2,
	/// <summary>A right-scroll arrow.</summary>
	Right = 3,
	/// <summary>A maximum-scroll arrow.</summary>
	Max = 3
}
